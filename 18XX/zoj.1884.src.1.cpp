// 无聊题...敲着敲着发现笨了...
// 写一个串，然后错位赋值就行了 

#include <cstdio>

char L[128] = { 0 };

int main() {
    
    L['1'] = '`';   L['2'] = '1';   L['3'] = '2';
    L['4'] = '3';   L['5'] = '4';   L['6'] = '5';
    L['7'] = '6';   L['8'] = '7';   L['9'] = '8';
    L['0'] = '9';   L['-'] = '0';   L['='] = '-';
    L['Q'] = '\t';  L['W'] = 'Q';   L['E'] = 'W';
    L['R'] = 'E';   L['T'] = 'R';   L['Y'] = 'T'; 
    L['U'] = 'Y';   L['I'] = 'U';   L['O'] = 'I'; 
    L['P'] = 'O';   L['['] = 'P';   L[']'] = '['; 
    L['\\'] = ']';  L['S'] = 'A';   L['D'] = 'S'; 
    L['F'] = 'D';   L['G'] = 'F';   L['H'] = 'G'; 
    L['J'] = 'H';   L['K'] = 'J';   L['L'] = 'K'; 
    L[';'] = 'L';   L['\''] = ';';  L['X'] = 'Z'; 
    L['C'] = 'X';   L['V'] = 'C';   L['B'] = 'V'; 
    L['N'] = 'B';   L['M'] = 'N';   L[','] = 'M'; 
    L['.'] = ',';   L['/'] = '.';   L[' '] = ' ';
    L['\n'] = '\n';
    
    char    c;
    while( ( c = getchar() ) != EOF )
        putchar( L[c] );
    
}
