#include <iostream>
#include <string>
using namespace std;

BinMatch    binmatch;

int     N,  S[5];

string  X[20],  input;

int     MP[128];

int main() {
    
    MP['S'] = 0;
    MP['M'] = 1;
    MP['L'] = 2;
    MP['X'] = 3;
    MP['T'] = 4;
    
    while( cin >> input && input == "START" ) {
        
        cin >> N;
        
        for( int i = 0; i < N; ++i )
            cin >> X[i];
            
        for( int i = 0; i < 5; ++i )
            cin >> S[i];
            
        cin >> input;
            
        binmatch.init( N, S[0] + S[1] + S[2] + S[3] + S[4] );
        
        int pos,    cnt; 
        for( cnt = pos = 0; pos < 5; ++pos ) {
            for( int i = 0; i < S[pos]; ++i, ++cnt ) {
                for( int j = 0; j < N; ++j ) {
                    int x = MP[X[j][0]],    y = MP[X[j][1]];
                    if( x <= pos && y >= pos )
                        binmatch.insert( j, cnt );
                }
            }
        }
        
        binmatch.run();
        
        puts( 
            binmatch.matchCnt() == N ?
            "T-shirts rock!" :
            "I'd rather not wear a shirt anyway..."
        );
        
    }
    
}
