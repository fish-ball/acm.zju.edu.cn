#include <iostream>
using namespace std;

BinMatch    binMatch; 

int main() {

    int T,  P,  N; 
    
    for( scanf( "%d", &T ); T--; ) {
        
        scanf( "%d%d", &P, &N );
        
        binMatch.init( P, N ); 
        
        for( int i = 1; i <= P; ++i ) {
            int k,  x;
            for( scanf( "%d", &k ); k--; ) {
                scanf( "%d", &x );
                binMatch.insert( i, x );
            }
        }
        
        binMatch.run(); 
        
        puts( binMatch.matchCnt() == P ? "YES" : "NO" );
                 
    } 
    
} 
